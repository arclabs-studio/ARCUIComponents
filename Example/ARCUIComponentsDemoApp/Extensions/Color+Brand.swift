import SwiftUI

/// ARC Labs Studio brand colors extension for demo app.
///
/// These colors match the official ARCDesignSystem brand colors.
extension Color {

    // MARK: Brand Colors

    /// ARC Labs primary brand color (Burgundy).
    ///
    /// - HEX: #541311
    /// - RGB: 84, 19, 17
    static let arcBrandBurgundy = Color(
        red: 84 / 255,
        green: 19 / 255,
        blue: 17 / 255
    )

    /// ARC Labs secondary brand color (Gold).
    ///
    /// - HEX: #FFB42E
    /// - RGB: 255, 180, 46
    static let arcBrandGold = Color(
        red: 255 / 255,
        green: 180 / 255,
        blue: 46 / 255
    )

    /// ARC Labs tertiary brand color (Black).
    ///
    /// - HEX: #000000
    /// - RGB: 0, 0, 0
    static let arcBrandBlack = Color(
        red: 0 / 255,
        green: 0 / 255,
        blue: 0 / 255
    )
}
